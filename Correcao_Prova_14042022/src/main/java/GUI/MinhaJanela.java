    private void jCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCalcularActionPerformed
        int conta;
        double saldoInicial;
        String tipo;                
       
        
       conta = Integer.parseInt(jNroConta.getText());
       saldoInicial = Double.parseDouble(jSaldo.getText());
       tipo = jTipo.getText();
              
       double saque, deposito;
       saque = Double.parseDouble(jSaque.getText());
       deposito = Double.parseDouble(jDeposito.getText());
       
       if (tipo.equals("P")) {
           ContaPoupanca minhaConta = new ContaPoupanca();
           
           minhaConta.numeroConta = conta;
           minhaConta.saldo = saldoInicial;
           minhaConta.tipoConta = tipo;
           
           if (saque > 0)
             minhaConta.FazerSaque(saque);
           
           if (deposito > 0)
             minhaConta.FazerDeposito(deposito);
           
           jLabelConta.setText(Integer.toString(minhaConta.numeroConta));
           jLabelSaldo.setText(Double.toString(minhaConta.saldo)); 
           jLabelTipo.setText("Poupan√ßa");
       }
       else {
           ContaCorrente minhaConta = new ContaCorrente();
           
           minhaConta.numeroConta = conta;
           minhaConta.saldo = saldoInicial;
           minhaConta.tipoConta = tipo;
           
           if (saque > 0)
             minhaConta.FazerSaque(saque);
           
           if (deposito > 0)
             minhaConta.FazerDeposito(deposito);
           
           jLabelConta.setText(Integer.toString(minhaConta.numeroConta));
           jLabelSaldo.setText(Double.toString(minhaConta.saldo)); 
           jLabelTipo.setText("Corrente");
       }
       
       
       
    }//GEN-LAST:event_jCalcularActionPerformed

